    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            float porcentagem = df.parse(ftfPorcentagemDesconto.getText()).floatValue();
            parametros.setDescontoPorcentagem(porcentagem);
            int e = JOptionPane.showConfirmDialog(this, "Deseja Alterar ?", "", JOptionPane.OK_CANCEL_OPTION);
            if (e == JOptionPane.OK_OPTION) {
                int r = gp.alterar(parametros);
                if (r == 1) {
                    JOptionPane.showMessageDialog(this, "Alterado com sucesso !");
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao alterar !");
            ftfPorcentagemDesconto.setText(String.valueOf(parametros.getJurosPorcentagem()));

        }


    }//GEN-LAST:event_jButton1ActionPerformed

